import 'package:flutter/material.dart';
import 'package:flutter_widgets/Common/ColorsApp.dart';

class StyleApps {
  StyleApps();

  TextStyle get textStyleGeneral => const TextStyle(
    color: textColors,
    fontFamily: "PlaywriteAT"
  );

  TextStyle get bodyStyle => textStyleGeneral.copyWith(
    fontWeight: FontWeight.w200
  );

  TextStyle get subtitleStyle => textStyleGeneral.copyWith(
    fontWeight: FontWeight.bold,
    color: textTermsColors
  );



  
// Bold Text Style
TextStyle boldTextStyle({
  int? size,
  Color? color,
  FontWeight? weight,
  String? fontFamily,
  double? letterSpacing,
  FontStyle? fontStyle,
  double? wordSpacing,
  TextDecoration? decoration,
  TextDecorationStyle? textDecorationStyle,
  TextBaseline? textBaseline,
  Color? decorationColor,
  Color? backgroundColor,
  double? height,
}) {
  return TextStyle(
    fontSize: size != null ? size.toDouble() : 20,
    letterSpacing: letterSpacing,
    fontStyle: fontStyle,
    decoration: decoration,
    decorationStyle: textDecorationStyle,
    decorationColor: decorationColor,
    wordSpacing: wordSpacing,
    textBaseline: textBaseline,
    backgroundColor: backgroundColor,
    height: height,
  );
}

